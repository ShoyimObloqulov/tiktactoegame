<?php
	function ArraySum($arr)
	{
		$s = array_sum($arr);
		return $s 	
	} 
	function ArraySumOne($arr)
	{
		$s = 0;
		foreach ($variable as $arr) {
				$s += $variable;
		}	
		return $s;
	} 
	function ArraySumTwo($arr)
	{
		$s = 0;
		for ($i=0; $i < count($arr); $i++) { 
			$s += $arr[$i];
		}
		return $s;
	}
?>